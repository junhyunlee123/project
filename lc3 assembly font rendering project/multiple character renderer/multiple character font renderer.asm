; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

;the program accomplishes its goal by having 3 loops. On each iteration of the line loop, we print each line. 
;Inside the line loop, we have character loop that prints multiple characters specified starting from 
;M[x5002] until null character is reached.
;On each character loop, we render each character by printing specified
;char on M[x5000] if each bit among the first 8 bits of font data line is 0. And print char on M[x5001]
;if each bit is 1. (starts from MSB to the first 8 bits sequentially and uses 8 iter loop).
;When we encounter null character on M[x500n] on chracter loop, we finish one iteration of line loop and go to
;the next line. (print 16 lines total to render a string)

;R0=ASCII value of the character which will be printed on the screen using OUT instruction to render the string starting from x5002
;R1=line number loop counter which goes from 0 to 16 to count the lines of bits printed on the screen
;R2=loop counter for counting bits of interest(first 8 bits) starting from MSB (goes from 8 to 0)
;R3=memory address of font data line of specificed character on M[x500n] starting from label address FONT_DATA
;R4=current font data line value we are working on
;R5=register for temporary use
;R6=register for storing memory addr of each character to be rendered

.ORIG x3000

	AND	R5, R5, x0000	;clear R5
	AND	R1, R1, x0000	;R1<-x0000

	

READ_NEW_LINE	LD	R6, CHAR_PRINT_ADDR	; R6<-x5002
	
	
;start of printing a character of the same line	
READ_NEW_CHAR	LDR	R5, R6, #0 	;R5<-M[R6], effectively R5<-M[x500n]

	BRz	END_LINE	; if null char, go to the next line

	AND	R3, R3, x0000	;R3<-x0000

	AND	R5, R5, xFFFF	; set the condition bit of R3
LINE_OFF_COND	BRz	SET_F_R3 ; if R5 == 0, PC=SET_F_R3	
	ADD	R3, R3, #8	; R3<-R3+8
	ADD	R3, R3, #8	; R3<-R3+8 (this and above line  same as R3<-R3+16 but cannot do it directely because of overflow)
	ADD	R5, R5, xFFFF	; R5<-R5-1
	BRnzp	LINE_OFF_COND	; jump to LINE_OFF_COND
				; the loop sets R3 = M[x500n]*16

SET_F_R3	LEA	R5, FONT_DATA	; R5 <- FONT_DATA	
	ADD	R3, R3, R5	; R3 <- R3+R5
				; sets R3 = M[x500n]*16 + (mem addr FONT_DATA)

	AND	R2, R2, x0000	; clear R2	
	ADD	R2, R2, x0008	; R2<-x0008 initialize & reset line bit counter
	ADD	R5, R3, R1	; R5 <- R3+R1
	LDR	R4, R5, x0000	; R4 <- M[R5], (this and above line same as R4 <- M[R3+R1])



	
CHECK_MSB_BIT	ADD	R4, R4, x0000	; set the condition bit for code in CHECK_MSB_BIT
	BRn	MSB_IS_ONE	; if MSB==1, PC=MSB_IS_ONE

	LDI	R0, BIT_ZERO_ADDR	; R0<-M[x5000]
	BRnzp	PRINT_PIXEL	; PC=PRINT_PIXEL to avoid doing R0<-M[x5001] on both cases
MSB_IS_ONE	LDI	R0, BIT_ONE_ADDR	; R0<-M[x5001]	

PRINT_PIXEL	OUT	

	ADD	R4, R4, R4	; R4<-R4*2 left shift R4 by 1
	
	ADD	R2, R2, xFFFF	; R2<-R2-1 (decrement pixel # counter by 1 on each line)
		
	BRp	CHECK_MSB_BIT	; check MSB that's left shifted (doing this checks every bit starting from original MSB)
	;end of printing a character of the same line
	
	ADD	R6, R6, #1	;
	BRnzp	READ_NEW_CHAR	; read new character of the same line

	END_LINE	AND	R0, R0, x0000	; clear R0
	ADD	R0, R0, x000A	; R0 <- R0 + x000A (this and above line same as R0<-x000A)
	OUT

	ADD	R1, R1, x0001	; R1<-R1+1

	ADD	R5, R1, xFFF0	; R5<-R1-16
	
	BRn	READ_NEW_LINE	; if R5<0, go to READ_NEW_LINE (this and above line make effectively the same condition as "if R1<16")
	
	HALT	; Halt the program if above branch is not taken (which is, if R1>=16)


