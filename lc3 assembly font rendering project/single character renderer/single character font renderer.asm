; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

;the program intends to read the first 8 bits of each font data line for 16 consecutive memory addresses(addr of font data lines) that corresponds to ascii
;character specified on x5002. We print ascii character specified on memory address x5000 if a bit on a particular font data line is 0 and
;print ascii character specified on x5001 if a bit on a particular font data line is 1. (on both cases, print on to the screen)

;We accomplish this by initally getting the address of first font data line of specified character on M[x5002] starting from label address FONT_DATA
;Then we use an iterative loop to read each font data line starting from the first line to the last line(16th line)
;On each iteration of the above outer loop, we read first 8 bits of a particular font data line starting from MSB by using iterative loop.
;In order to read each bit while iterating on above loop, we left-shift value of the current font data line by 1 at the end of each above iteration and
;use BRn to check MSB value of shifting value of the current font data line. (if branch not taken, MSB=0, if branch taken, MSB=1)
;If the read MSB is 0, print the ascii value stored on memory address x5000 on the screen and if the read MSB
;is 1, print the ascii value stored on memory address x5001 on the screen. When you finished printing all the data on a particular line, we print x0A to 
;go to the next line for printing. Then we go back to the outer loop to read the next font data line or end the program after incrementing a line counter (0~16)

;R0=ASCII value of the character which will be printed on the screen using OUT instruction
;R1=line number loop counter which goes from 0 to 16 to count the lines of bits printed on the screen
;R2=loop counter for counting bits of interest(first 8 bits) starting from MSB (goes from 8 to 0)
;R3=memory address of first font data line of specificed character on M[x5002] starting from label address FONT_DATA
;R4=current font data line value we are working on
;R5=register for temporary use
 

.ORIG x3000

	AND	R5, R5, x0000	;clear R5
	AND	R1, R1, x0000	;R1<-x0000

	AND	R3, R3, x0000	;R3<-0000
	LDI	R5, CHAR_PRINT_ADDR	;R5<-M[x5002]	PC=x3004 R5=temporary ascii offset counter
LINE_OFF_COND	BRz	SET_F_R3 ; if R5 == 0, PC=SET_F_R3	
	ADD	R3, R3, #8	; R3<-R3+8
	ADD	R3, R3, #8	; R3<-R3+8 (this and above line  same as R3<-R3+16 but cannot do it directely because of overflow)
	ADD	R5, R5, xFFFF	; R5<-R5-1
	BRnzp	LINE_OFF_COND	; jump to LINE_OFF_COND
				; the loop sets R3 = M[x5002]*16
SET_F_R3	
	LEA	R5, FONT_DATA	; R5 <- FONT_DATA	
	ADD	R3, R3, R5	; R3 <- R3+R5
				; sets R3 = M[x5002]*16 + (mem addr FONT_DATA)
				; end of initilizing fixed-value registers

READ_NEW_LINE	AND	R2, R2, x0000	; clear R2	
	ADD	R2, R2, x0008	; R2<-x0008 initialize & reset line bit counter
	ADD	R5, R3, R1	; R5 <- R3+R1
	LDR	R4, R5, x0000	; R4 <- M[R5], (this and above line same as R4 <- M[R3+R1])
	
CHECK_MSB_BIT	ADD	R4, R4, x0000	; set the condition bit for code in CHECK_MSB_BIT
	BRn	MSB_IS_ONE	; if MSB==1, PC=MSB_IS_ONE

	LDI	R0, BIT_ZERO_ADDR	; R0<-M[x5000]
	BRnzp	PRINT_PIXEL	; PC=PRINT_PIXEL to avoid doing R0<-M[x5001] on both cases
MSB_IS_ONE	LDI	R0, BIT_ONE_ADDR	; R0<-M[x5001]	

PRINT_PIXEL	OUT	

	ADD	R4, R4, R4	; R4<-R4*2 left shift R4 by 1
	
	ADD	R2, R2, xFFFF	; R2<-R2-1 (decrement pixel # counter by 1 on each line)
		
	BRp	CHECK_MSB_BIT	; check MSB that's left shifted (doing this checks every bit starting from original MSB)

	AND	R0, R0, x0000	; clear R0
	ADD	R0, R0, x000A	; R0 <- R0 + x000A (this and above line same as R0<-x000A)
	OUT

	ADD	R1, R1, x0001	; R1<-R1+1

	ADD	R5, R1, xFFF0	; R5<-R1-16
	
	BRn	READ_NEW_LINE	; if R5<0, go to READ_NEW_LINE (this and above line make effectively the same condition as "if R1<16")
	
	HALT	; Halt the program if above branch is not taken (which is, if R1>=16)


